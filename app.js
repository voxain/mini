const createError = require('http-errors');
const express = require('express');
const path = require('path');
const cookieParser = require('cookie-parser');
const logger = require('morgan');
const fs = require('fs');

const indexRouter = require('./routes/index');
const { config } = require('process');

const app = express();

// view engine setup
app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'ejs');

app.use(logger('dev'));
app.use(express.json());
app.use(express.urlencoded({ extended: false }));
app.use(cookieParser());
app.use(express.static(path.join(__dirname, 'public')));

app.use('/', indexRouter);

// Create empty shortend.json if it does not already exists
if (!fs.existsSync('shortened.json')) fs.writeFileSync('shortened.json', '{}')

// catch 404 and forward to error handler
app.use(function (req, res, next) {
  next(createError(404));
});

// error handler
app.use(function (err, req, res) {
  // set locals, only providing error in development
  res.locals.message = err.message;
  res.locals.error = req.app.get('env') === 'development' ? err : {};

  // render the error page
  res.status(err.status || 500);
  res.render('error');
});

module.exports = app;

// Clear old links
setInterval(() => {
  const currentShorts = require('./shortened.json');
  Object.entries(currentShorts).forEach((link) => {
    if (
      (new Date() - new Date(link[1].created.at)) / 1000 / 60 / 60 >
      config.expiration
    )
      delete currentShorts[link[0]];
  });
  fs.writeFileSync('shortened.json', JSON.stringify(currentShorts));
}, 1000 * 60);